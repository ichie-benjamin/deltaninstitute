                        <h3 class="title"><a href="#">College of Education, Agbor, Delta State, Journal of Studies in Education. <br><br>Preamble</a></h3>
                        <div style="clear: both;">&nbsp;</div>
                        <div class="entry">
                            <p><br>
                                The Journal of Studies in Education (JOSTED) is an open-access multi-disciplinary peer-reviewed Journal devoted to the publication of original (empirical and theoretical) research papers, critical, up-to-date and concise reviews on all themes related to all aspects of Education..

                            <h3 class="title"><a href="#">Guidelines for the submission of Articles</a></h3>
                            <p>
                            <ol>
                                <li>Articles should not be more than ten (10) pages (including references/tables/figures).
                                    It is preferred that the whole article should be  prepared in Microsoft word and in the following order, Title of article, contributor’s (s)name(s) in full with surname first, the affiliation of each author and the detailed address (with e-mail and phone number) of the corresponding author.
                                </li>
                                <li>	Article must have an abstract not exceeding 200 words. It should be written in simple language, clear and concise, and should give the scope, purpose, procedures, significant results and conclusion of the work.
                                </li>
                                <li>Table and figures must have appropriate and comprehensive captions and numbered consecutively with Arabic numerals.
                                </li>
                                <li>All words cited in the text must be listed under reference in alphabetical order of author’s surname.
                                </li>
                                <li>Footnotes are not permitted.</li>
                                <li>The latest referencing style of APA or MLA should be used.</li>
                                <li>Submission of a manuscript will be held to imply that the work reported in it is original; that the results have not been previously published and are not under consideration for publication elsewhere.
                                </li>
                                <li>Accepted manuscript will (as a whole and in part) become the property of Journal of Studies in Education which will own the entirety of the copyright.
                                </li>

                            </ol>
                            <p> <b>N:B</b><br>Manuscripts should be submitted electronically as e-mail attachment provided the text; tables and figures are included in a single Microsoft word file via the e-mail.<br><br></p>

