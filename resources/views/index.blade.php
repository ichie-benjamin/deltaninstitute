                        <h3 class="title"><a href="#">Brief History of COE-Agbor</a></h3>

                        <div style="clear: both;">&nbsp;</div>
                        <div class="entry">
                            <p>
                                College of Education, Agbor, Delta State was established in October, 1979 by the former Bendel State Government, to engage in the training of professional teachers; a training that shall lead to the award of the Nigeria Certificate in Education (NCE). On Tuesday, 18th August, 1981, the Late Professor Ambrose F. Alli, the then Governor of former Bendel State, performed the official opening of the College. With the creation of Edo and Delta States on August, 27th 1991, the College is now owned by the Delta State Government.</p>

